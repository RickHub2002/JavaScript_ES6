//Igual (==)
//Retorna verdadeiro caso os operadores sejam iguais. -3 == var1
"3" == var1
3 == '3'

//N達o igual (!=)
//Retorna verdadeiro caso os operandos n達o sejam iguais. var1 != 4
var2 != "3"

//Estritamente igual (===)
//Retorna verdadeiro caso os tipos e o valor dos operandos sejam iguais
3 === var1

//Estritamente diferente (!==)
//Retorna verdadeiro caso os operandos n達o sejam iguais e/ou n達o sejam do mesmo tipo.
var1 !== '3'
3 !== '3'

//maior que (>)
//Retorna verdadeiro caso o operando da esquerda seja maior que o da direita.
var2 > var1
"12" > 2

